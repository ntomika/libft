#include "libft.h"

void	*ft_memchr(const void *src, int c, size_t n)
{
	char	*p;
	size_t	i;

	p = (char *)src;
	i = 0;
	while (i < n)
	{
		if (p[i] == (char)c)
			return ((void *)p + i);
		i++;
	}
	return (NULL);
}
