#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (100);
	if (c >= '0' && c <= '9')
		return (100);
	return (0);
}
