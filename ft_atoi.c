#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	unsigned long	r;
	int				z;

	i = 0;
	r = 0;
	z = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			z = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		r = r * 10 + (str[i] - '0');
		i++;
	}
	if (r >= 9223372036854775807UL)
		return (z == 1 ? -1 : 0);
	return (r * z);
}
